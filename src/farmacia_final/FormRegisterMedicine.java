        public void clean(){
        this.txtCodigo.setText("");
        this.txtNombre.setText("");
        this.txtDescripcion.setText("");
        this.txtPrecio.setText("");
        this.txtStock.setText("");
        this.txtProveedor.setText("");
        this.txtCod_tipo.setText("");
        this.txtNombre_Tipo.setText("");
    }
    
    public void rellenar(String str){
               
        this.txtCodigo.setText(obj.getValueFromToString(str, "Code"));
        this.txtNombre.setText(obj.getValueFromToString(str, "Name"));
        this.txtDescripcion.setText(obj.getValueFromToString(str, "Description"));
        this.txtPrecio.setText(obj.getValueFromToString(str, "Price"));
        this.txtStock.setText(obj.getValueFromToString(str, "Stock"));
        this.txtProveedor.setText(obj.getValueFromToString(str, "Supplier"));
        this.txtCod_tipo.setText(obj.getValueFromToString(str, "Code_type"));
        this.txtNombre_Tipo.setText(obj.getValueFromToString(str, "Name_type"));
       
        
        System.out.println(str);
       
    
    }
    
    public  void llenar_combo(Tipos[] tiposArray){
           comboBOX.removeAllItems(); // Limpia los elementos existentes en el JComboBox
    
       
        
    if (tiposArray != null) {
        for (Tipos tipo : tiposArray) {
            if (tipo != null) {
                comboBOX.addItem(tipo.getName_type());
                //System.out.println("Agregado a comboBOX: " + tipo.getName_type());
            }
        }
    } else {
        System.out.println("tiposArray es nulo");
        }
    }
    
    
